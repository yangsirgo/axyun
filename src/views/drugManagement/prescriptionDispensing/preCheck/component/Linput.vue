<template>
  <div>
    <el-popover
      placement="bottom"
      :width="popoverWidth"
      trigger="manual"
      v-model="popverVisbile"
    >
      <el-table
        ref="singleTable"
        :data="tableData"
        highlight-current-row
        style="width: 100%"
        v-loading="tableLoading"
        element-loading-text="列表正在加载中"
        @row-click="selectHandler"
        height="300"
      >
        <el-table-column
          class="overflow-point"
          header-align="center"
          v-for="(item, index) in tableParams"
          :key="index"
          :prop="item.prop"
          :label="item.label"
          :min-width="item.width"
          align="center"
          :fixed="item.fixed === true"
          show-overflow-tooltip
        >
          <template slot-scope="{ row }">
            <template v-if="item.prop === 'drugStock'">
              <span>{{ row.kcOut }}</span>
              <span :val="row.drugUnit" code="DrugUnit" v-codeTransform></span>
              <template v-if="row.kcBl !== 0">
                <span>{{ row.kcBl }}</span>
                <span
                  :val="row.packUnit"
                  code="DrugUnit"
                  v-codeTransform
                ></span>
              </template>
            </template>
            <template v-else-if="item.prop === 'originCode'">
              <span
                :val="row.originCode"
                code="Manufacturer"
                v-codeTransform
              ></span>
            </template>
            <template v-else-if="item.prop === 'formCode'">
              <span
                tableName="hrp_form"
                :conditionMap="{ form_code: row.formCode }"
                columns="form_name"
                v-tableTransform
              ></span>
            </template>
            <template v-else-if="item.prop === 'purchasePrice'">
              <span>{{ formatNum(row.purchasePrice) }}</span>
            </template>
            <template v-else-if="item.prop === 'price'">
              <span>{{ formatNum(row.price) }}</span>
            </template>
            <span v-else class="overflow-point">{{ row[item.prop] }}</span>
          </template>
        </el-table-column>
      </el-table>
      <el-input
        slot="reference"
        ref="singleInput"
        v-model="inputValue"
        :placeholder="placeholder"
        @blur="hidePopover"
        @click.native="searchHandler"
        @keyup.native="searchHandler"
      ></el-input>
    </el-popover>
  </div>
</template>
<script>
import hmmBaseApi from "@/api/hmm/base";
import formatNum from "@/utils/formatPrice.js";

export default {
  props: {
    popoverWidth: {
      type: Number,
      default: 400
    },
    tableData: {
      type: Array,
      default: () => [],
      required: true
    },
    tableParams: {
      type: Array,
      default: () => [],
      required: true
    },
    tableLoading: {
      type: Boolean,
      default: false
    },
    placeholder: {
      type: String,
      default: "请输入内容"
    },
    backDrugName: {
      type: String
    }
  },
  data() {
    return {
      popverVisbile: false,
      searchTimer: null,
      inputValue: "",
      curIdx: null
    };
  },
  watch: {
    backDrugName: {
      handler(val) {
        if (val !== this.inputValue) {
          this.inputValue = val;
        }
      },
      immediate: true
    }
  },
  methods: {
    formatNum(num) {
      return formatNum(num);
    },
    searchHandler(event) { 
      if (this.inputValue !== event.target.value) {
        this.inputValue = event.target.value;
      }
      if (this.tableLoading && this.popverVisbile) {
        return;
      }
      this.$emit("sendVal", this.inputValue);
      // 按键下
      if (event.keyCode === 40) {
        this.tableKeyDown(1);
        return;
      }
      // 按键上
      if (event.keyCode === 38) {
        this.tableKeyDown(-1);
        return;
      }
      // 确认键
      if (event.keyCode === 13) {
        if (this.curIdx !== null) {
          this.tableKeyEnter();
        } else {
          this.showPopover();
          this.$emit("query", this.inputValue);
        }
        return;
      }
      // if (event.keyCode === 16) {
      //   if (this.inputValue !== "") {
      // this.query();
      //   }
      //   return;
      // }
      // 过滤非值的按键
      if (!this.isValueKey(event.keyCode)) {
        return;
      }

      if (this.searchTimer) {
        clearTimeout(this.searchTimer);
        this.clearTimeout = null;
      }
      this.searchTimer = setTimeout(this.query, 500);
    },
    isValueKey(code) {
      return (
        (code >= 48 && code <= 57) ||
        (code >= 65 && code <= 90) ||
        (code >= 96 && code <= 111) ||
        (code >= 186 && code <= 192) ||
        code == 8
      );
    },
    query() {
      this.curIdx = null;
      this.$emit("query", this.inputValue);
      this.showPopover();
    },
    showPopover() {
      this.popverVisbile = true;
    },
    hidePopover() {
      this.popverVisbile = false;
      this.curIdx = null;
      this.$emit("blur");
    },
    tableKeyDown(val) {
      if (this.curIdx === null) {
        this.curIdx = 0;
      } else if (val > 0 && this.curIdx >= this.tableData.length - 1) {
        this.query();
      } else if (val < 0 && this.curIdx <= 0) {
        return;
      } else {
        this.curIdx += val;
      }
      this.setTableCur();
    },
    tableKeyEnter() {
      if (this.tableData.length > 0) {
        this.$emit("select", this.tableData[this.curIdx]);
        this.inputValue = this.tableData[this.curIdx].drugName;
      }
      this.hidePopover();
    },
    selectHandler(row, index) {
      this.$emit("select", row);
      this.hidePopover();
    },
    setTableCur() {
      if (this.$refs.singleTable) {
        this.$refs.singleTable.setCurrentRow(this.tableData[this.curIdx]);
      }
      this.scrollToRight();
    },
    focus() {
      if (this.$refs.singleInput) {
        this.$refs.singleInput.focus();
      }
    },
    scrollToRight() {
      if (this.$refs.singleTable) {
        if (this.curIdx <= 2 || this.curIdx >= this.tableData.length - 3) {
          return;
        }
        this.$refs.singleTable.bodyWrapper.scrollTop = (this.curIdx - 2) * 24;
      }
    }
  }
};
</script>
